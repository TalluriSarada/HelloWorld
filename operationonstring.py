# Demonstrating min() - Example 1:
print(min("aAbByYzZ"))#A  finds the minimum element of the sequence passed as an argument. 


# Demonstrating min() - Examples 2 & 3:
t = 'The Knights Who Say "Ni!"'
print('[' + min(t) + ']') #[]

t = [0, 1, 2]
print(min(t))#0

# Demonstrating max() - Example 1:
print(max("aAbByYzZ")) #z finds the maximum element of the sequence.


# Demonstrating max() - Examples 2 & 3:
t = 'The Knights Who Say "Ni!"'
print('[' + max(t) + ']') #[Y]

t = [0, 1, 2]
print(max(t)) #Z
